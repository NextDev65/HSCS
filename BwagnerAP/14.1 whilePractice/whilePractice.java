class whilePractice
{
	public static void main(String[] args)
	{
		// counter
		int ctr = 1;
		// while1
		while (ctr<=25)
		{
			System.out.print(ctr+ " ");
			ctr++;
		}
		System.out.println("\n\n");
		// while2
		ctr = 50;
		while (ctr<=70)
		{
			System.out.print(ctr+ " ");
			ctr++;
		}
		System.out.println("\n\n");
		// while3
		ctr = 0;
		while (ctr<=100)
		{
			System.out.print(ctr+ " ");
			ctr+=5;
		}
		System.out.println("\n\n");
		// while4
		ctr = 20;
		while (1<=ctr)
		{
			System.out.print(ctr+ " ");
			ctr--;
		}
		System.out.println("\n");
		// computer
		ctr = 15;
		while (1<=ctr)
		{
			System.out.println("computer");
			ctr--;
		}
	}
}