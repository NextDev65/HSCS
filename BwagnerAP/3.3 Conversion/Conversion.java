class Conversion
{
  public static void main ( String[] args )
  {
  	System.out.println("       Units and Conversion Factors");
  	System.out.println("----------------------------------------------");
  	double inch = 2.54;
  	double yard = 0.9144;
  	double mile = 1.609;
  	double centimeter = 0.3937;
  	double meter = 1.094;
  	double kilometer = 0.6214;
  	double lightYear = 9.464E12;
  	System.out.println("inch = " +inch+ " centimeters");
  	System.out.println("yard = " +yard+ " meters");
  	System.out.println("mile = " +mile+ " kilometers");
  	System.out.println("centimeter = " +centimeter+ " inches");
  	System.out.println("meter = " +meter+ " yards");
  	System.out.println("kilometer = " +kilometer+ " miles");
  	System.out.println("lightYear = " +lightYear+ " kilometers");
  }
}